// Ternary Operator

// Another form of conditional statements is the ternary operator.

// Syntax:

// condition ? true-block : false-block

// Example:

var x = 10;

x % 2 === 0 ? console.log("Even") : console.log("Odd");

// Example:

var x = 10;

console.log(x % 2 === 0 ? "Even" : "Odd");

// Example:

var city = "Jaipur";

if (city === 'New Delhi') {
   console.log('Capital of India');
} else {
   console.log('Not a capital of India');
}



